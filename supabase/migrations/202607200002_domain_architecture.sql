alter table public.quote_items add column if not exists amount_cents bigint;
alter table public.invoice_items add column if not exists rate_cents bigint;
alter table public.invoice_items add column if not exists client_rate_cents bigint;
alter table public.invoice_items add column if not exists amount_cents bigint;
alter table public.expenses add column if not exists amount_cents bigint;

update public.quotes set
  subtotal_cents = round(coalesce(subtotal, 0) * 100),
  total_cents = round(coalesce(total, 0) * 100)
where subtotal_cents is null or total_cents is null;
update public.invoices set
  subtotal_cents = round(coalesce(subtotal, 0) * 100),
  total_cents = round(coalesce(total, 0) * 100)
where subtotal_cents is null or total_cents is null;
update public.quote_items set
  rate_cents = round(coalesce(rate, 0) * 100),
  client_rate_cents = round(coalesce(client_rate, rate, 0) * 100),
  amount_cents = round(coalesce(amount, 0) * 100)
where rate_cents is null or client_rate_cents is null or amount_cents is null;
update public.invoice_items set
  rate_cents = round(coalesce(rate, 0) * 100),
  client_rate_cents = round(coalesce(client_rate, rate, 0) * 100),
  amount_cents = round(coalesce(amount, 0) * 100)
where rate_cents is null or client_rate_cents is null or amount_cents is null;
update public.expenses set amount_cents = round(coalesce(amount, 0) * 100) where amount_cents is null;

