create table if not exists public.document_events (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  document_type text not null check (document_type in ('quote', 'invoice')),
  document_id uuid not null,
  event_type text not null,
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now()
);
create index if not exists document_events_document_idx
  on public.document_events(user_id, document_type, document_id, created_at desc);
alter table public.document_events enable row level security;
drop policy if exists "app_document_events_owner" on public.document_events;
create policy "app_document_events_owner" on public.document_events
  for select to authenticated using (user_id = (select auth.uid()));

create or replace function public.record_document_status_event()
returns trigger language plpgsql security definer set search_path = '' as $$
begin
  if tg_op = 'INSERT' then
    insert into public.document_events(user_id, document_type, document_id, event_type, metadata)
    values (new.user_id, case when tg_table_name = 'quotes' then 'quote' else 'invoice' end,
      new.id, 'created', jsonb_build_object('status', new.status));
  elsif new.status is distinct from old.status then
    insert into public.document_events(user_id, document_type, document_id, event_type, metadata)
    values (new.user_id, case when tg_table_name = 'quotes' then 'quote' else 'invoice' end,
      new.id, 'status_changed', jsonb_build_object('from', old.status, 'to', new.status));
  end if;
  return new;
end;
$$;
revoke all on function public.record_document_status_event() from public;
drop trigger if exists record_quote_status_event on public.quotes;
create trigger record_quote_status_event after insert or update of status on public.quotes
  for each row execute function public.record_document_status_event();
drop trigger if exists record_invoice_status_event on public.invoices;
create trigger record_invoice_status_event after insert or update of status on public.invoices
  for each row execute function public.record_document_status_event();

create or replace function public.save_quote_with_items(document jsonb, items jsonb)
returns uuid language plpgsql set search_path = '' as $$
declare
  owner_id uuid := auth.uid();
  document_id uuid;
  item jsonb;
begin
  if owner_id is null then raise exception 'Authentication required' using errcode = '42501'; end if;
  if nullif(document->>'client_id', '') is not null and not exists (
    select 1 from public.clients where id = (document->>'client_id')::uuid and user_id = owner_id
  ) then
    raise exception 'Client not found' using errcode = 'P0002';
  end if;
  document_id := nullif(document->>'id', '')::uuid;
  if document_id is null then
    insert into public.quotes(
      user_id, client_id, quote_num, status, date, valid_days, job_desc,
      client_name, client_phone, client_email, job_address,
      biz_name, biz_phone, biz_email, biz_address, biz_license, notes,
      tax_enabled, tax_rate, subtotal, total, overhead_percent, overhead_visible,
      subtotal_cents, overhead_cents, tax_cents, total_cents
    ) values (
      owner_id, nullif(document->>'client_id','')::uuid, document->>'quote_num', document->>'status',
      nullif(document->>'date','')::date, coalesce((document->>'valid_days')::int,30), document->>'job_desc',
      document->>'client_name', document->>'client_phone', document->>'client_email', document->>'job_address',
      document->>'biz_name', document->>'biz_phone', document->>'biz_email', document->>'biz_address', document->>'biz_license', document->>'notes',
      coalesce((document->>'tax_enabled')::boolean,false), coalesce((document->>'tax_rate')::numeric,0),
      coalesce((document->>'subtotal')::numeric,0), coalesce((document->>'total')::numeric,0),
      coalesce((document->>'overhead_percent')::numeric,0), coalesce((document->>'overhead_visible')::boolean,false),
      coalesce((document->>'subtotal_cents')::bigint,0), coalesce((document->>'overhead_cents')::bigint,0),
      coalesce((document->>'tax_cents')::bigint,0), coalesce((document->>'total_cents')::bigint,0)
    ) returning id into document_id;
  else
    update public.quotes set
      client_id=nullif(document->>'client_id','')::uuid, quote_num=document->>'quote_num', status=document->>'status',
      date=nullif(document->>'date','')::date, valid_days=coalesce((document->>'valid_days')::int,30), job_desc=document->>'job_desc',
      client_name=document->>'client_name', client_phone=document->>'client_phone', client_email=document->>'client_email', job_address=document->>'job_address',
      biz_name=document->>'biz_name', biz_phone=document->>'biz_phone', biz_email=document->>'biz_email', biz_address=document->>'biz_address',
      biz_license=document->>'biz_license', notes=document->>'notes', tax_enabled=coalesce((document->>'tax_enabled')::boolean,false),
      tax_rate=coalesce((document->>'tax_rate')::numeric,0), subtotal=coalesce((document->>'subtotal')::numeric,0),
      total=coalesce((document->>'total')::numeric,0), overhead_percent=coalesce((document->>'overhead_percent')::numeric,0),
      overhead_visible=coalesce((document->>'overhead_visible')::boolean,false), subtotal_cents=coalesce((document->>'subtotal_cents')::bigint,0),
      overhead_cents=coalesce((document->>'overhead_cents')::bigint,0), tax_cents=coalesce((document->>'tax_cents')::bigint,0),
      total_cents=coalesce((document->>'total_cents')::bigint,0), updated_at=now()
    where id=document_id and user_id=owner_id;
    if not found then raise exception 'Quote not found' using errcode='P0002'; end if;
  end if;
  delete from public.quote_items where quote_id=document_id;
  for item in select value from jsonb_array_elements(coalesce(items,'[]'::jsonb)) loop
    insert into public.quote_items(quote_id,sort_order,description,unit,qty,rate,amount,source_name,note,line_type,markup_enabled,markup_mode,markup_value,client_rate,rate_cents,client_rate_cents,amount_cents)
    values(document_id,coalesce((item->>'sort_order')::int,0),item->>'description',item->>'unit',coalesce((item->>'qty')::numeric,0),
      coalesce((item->>'rate')::numeric,0),coalesce((item->>'amount')::numeric,0),item->>'source_name',item->>'note',item->>'line_type',
      coalesce((item->>'markup_enabled')::boolean,false),item->>'markup_mode',coalesce((item->>'markup_value')::numeric,0),coalesce((item->>'client_rate')::numeric,0),
      coalesce((item->>'rate_cents')::bigint,0),coalesce((item->>'client_rate_cents')::bigint,0),coalesce((item->>'amount_cents')::bigint,0));
  end loop;
  return document_id;
end;
$$;

